public class Product
{
     public string _name;
     public int _productID;
     public double _price;
     public int _quantityOfProduct;

    public Product(string name, int productID, double price, int _quantityOfProduct)
    {
       _name = name;
       _productID = productID;
       _price = price;
       _quantityOfProduct = _quantityOfProduct;
    }
}